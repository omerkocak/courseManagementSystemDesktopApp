    public void fillComboTeachers() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection(b.getUrl(), b.getRoot(), b.getSifre());
            String sorgu = "select idogretmen,email from ogretmen";
            PreparedStatement pst = con.prepareStatement(sorgu);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                aid = rs.getInt("idogretmen"); //alıcı id ataması
                Aliciemail = rs.getString("email");//alıcı email atama
                comboTeachers.addItem(Aliciemail); //comboboxı doldurma                
            }
            con.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

