    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        // TODO add your handling code here:
        Object obj = comboTeachers.getSelectedItem();
        Object obj2 = comboStudents.getSelectedItem();
        //comboboxtan email bilgisini çekme
        if (!(obj.equals(""))) { //öğretmen seçilmişse
            Aliciemail = String.valueOf(obj);
            //alici id bulma
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection(b.getUrl(), b.getRoot(), b.getSifre());
                String sorgu = "select idogretmen from ogretmen where email='" + Aliciemail + "'";
                PreparedStatement pst = con.prepareStatement(sorgu);
                ResultSet rs = pst.executeQuery();
                while (rs.next()) {
                    aid = rs.getInt("idogretmen");
                }
                flag = false;
                con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else if (!(obj2.equals(""))) {//ogrenci seçilmiş ise
            Aliciemail = String.valueOf(obj2);
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection(b.getUrl(), b.getRoot(), b.getSifre());
                String sorgu = "select idogrenci from ogrenci where email='" + Aliciemail + "'";
                PreparedStatement pst = con.prepareStatement(sorgu);
                ResultSet rs = pst.executeQuery();
                while (rs.next()) {
                    aid = rs.getInt("idogrenci");
                }
                flag = false;
                con.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        } else {
            flag = true;
            JOptionPane.showMessageDialog(null, "Please select receiver mail!");
        }
        if (flag == false) {
            try {
                Class.forName("com.mysql.cj.jdbc.Driver");
                Connection con = DriverManager.getConnection(b.getUrl(), b.getRoot(), b.getSifre());
                String sorgu = "insert into mesaj(aliciid,gondericiid,gondericiEmail,tarih,metin) values(?,?,?,?,?)";
                PreparedStatement pst = con.prepareStatement(sorgu);
                pst.setInt(1, aid); //alici id ekleme
                pst.setInt(2, id); //gonderici id
                pst.setString(3, gonderciMail); //gonderici email
                SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss");
                Date time = new Date();
                pst.setString(4, formatter.format(time));
                pst.setString(5, txtAreaWriteM.getText());  
                
                pst.execute();
                con.close();
                JOptionPane.showMessageDialog(null, "Message sent successfully!");
                txtAreaWriteM.setText("");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_btnSendActionPerformed

